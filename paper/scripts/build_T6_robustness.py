    parser.add_argument("--t2_qa", default="paper/figdata/T2_QA.json")
    parser.add_argument("--f3_qa", default="paper/figdata/F3_QA.json")
    parser.add_argument("--f4_qa", default="paper/figdata/F4_QA.json")
